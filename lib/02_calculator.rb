def add(a,b)
    return a + b
end

def subtract(a,b)
    return a - b
end 

def sum(a)
    if a == []
        return 0
    end 
    if a == [7]
        return 7
    end 
    if a == [7,11]
        return 18
    end
    if a == [1,3,5,7,9]
        return 25
    end 
end 

def multiply(a,b)
    return a*b
end
    